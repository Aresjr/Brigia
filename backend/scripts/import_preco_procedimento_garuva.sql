procedimentos_resolvidos AS (
    SELECT
        df.*,
        COALESCE(p_codigo.id, p_nome.id) AS procedimento_id
    FROM dados_filtrados df
    LEFT JOIN LATERAL (
        SELECT id
        FROM procedimento
        WHERE codigo = df.codigo_tuss
        LIMIT 1
    ) AS p_codigo ON TRUE
    LEFT JOIN LATERAL (
        SELECT id
        FROM procedimento
        WHERE upper(nome) = df.procedimento_nome_upper
        LIMIT 1
    ) AS p_nome ON TRUE
),
convenio_alias AS (
    SELECT 'VALE +/MENSALISTA' AS raw, ARRAY['VALUE+','MENSALISTA'] AS nomes UNION ALL
    SELECT 'VALE+/MENSALISTA' AS raw, ARRAY['VALUE+','MENSALISTA'] AS nomes UNION ALL
    SELECT 'PARCEIRO/SUS/ PREV SAUDE' AS raw, ARRAY['PARCEIRO/SUS','PREV SAUDE'] AS nomes
),
convenios_expandido AS (
    SELECT
        pr.*,
        NULLIF(btrim(nome), '') AS convenio_nome_normalizado
    FROM procedimentos_resolvidos pr
    LEFT JOIN convenio_alias ca ON upper(ca.raw) = upper(pr.convenio_bruto)
    CROSS JOIN LATERAL unnest(
        CASE
            WHEN ca.nomes IS NOT NULL THEN ca.nomes
            ELSE regexp_split_to_array(pr.convenio_bruto, '/')
        END
    ) AS nome
),
convenios_resolvidos AS (
    SELECT
        ce.*,
        c.id AS convenio_id
    FROM convenios_expandido ce
    LEFT JOIN convenio c ON upper(c.nome) = upper(ce.convenio_nome_normalizado)
),
dados_validos AS (
    SELECT DISTINCT
        procedimento_id,
        convenio_id,
        preco
    FROM convenios_resolvidos
    WHERE procedimento_id IS NOT NULL
      AND convenio_id IS NOT NULL
),
atualizados AS (
    UPDATE preco_procedimento pp
       SET preco = dv.preco
    FROM dados_validos dv
    WHERE pp.procedimento_id = dv.procedimento_id
      AND pp.convenio_id = dv.convenio_id
      AND pp.unidade_id = 2
    RETURNING pp.id
),
inseridos AS (
    INSERT INTO preco_procedimento (procedimento_id, convenio_id, unidade_id, preco)
    SELECT dv.procedimento_id, dv.convenio_id, 2, dv.preco
    FROM dados_validos dv
    WHERE NOT EXISTS (
        SELECT 1
        FROM preco_procedimento pp
        WHERE pp.procedimento_id = dv.procedimento_id
          AND pp.convenio_id = dv.convenio_id
          AND pp.unidade_id = 2
    )
    RETURNING id
)
SELECT
    (SELECT count(*) FROM dados_validos) AS total_processados,
    (SELECT count(*) FROM atualizados) AS precos_atualizados,
    (SELECT count(*) FROM inseridos) AS precos_inseridos;

